/**
 * ----- indexing -----
 * 
 * ==> 0 indexing
 * 
 * ==> ith index element: array[i]
 * 
 * ==> 3 no element: index 2
 * 
 * ==> set or update element value by index
 */

let numbers = [1, 5, 66, 45, 65, 65, 88, 11, 45, 65, 65, 88, 15];

console.log(numbers);
console.log(numbers[2]);

const fourth = numbers[3]
console.log(fourth);

// update
numbers[3] = 4;
console.log(numbers[3]);